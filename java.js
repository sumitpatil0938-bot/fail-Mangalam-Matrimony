let isPhoneVerified = false;
let verifiedPhone = "";

function getAge(dateOfBirth) {
    const today = new Date();
    const birthDate = new Date(dateOfBirth);
    let age = today.getFullYear() - birthDate.getFullYear();
    const monthDifference = today.getMonth() - birthDate.getMonth();

    if (monthDifference < 0 || (monthDifference === 0 && today.getDate() < birthDate.getDate())) {
        age -= 1;
    }

    return age;
}

function normalizePhone(phone) {
    return phone.replace(/\D/g, "");
}

function showAccountMessage(message, type) {
    accountMessage.textContent = message;
    accountMessage.classList.remove("success", "error");
    accountMessage.classList.add("show", type);
}

function showPhoneStatus(message, type) {
    phoneStatus.textContent = message;
    phoneStatus.classList.remove("success", "error");
    phoneStatus.classList.add(type);
}

function resetPhoneVerification() {
    isPhoneVerified = false;
    verifiedPhone = "";
    showPhoneStatus("Phone verification is not required for authentication yet.", "error");
}

function fillStateOptions() {
    if (!stateSelect) {
        return;
    }

    Object.keys(stateCities).forEach((state) => {
        const option = document.createElement("option");
        option.value = state;
        option.textContent = state;
        stateSelect.appendChild(option);
    });
}

function fillCityOptions(state) {
    citySelect.innerHTML = '<option value="">Select district</option>';
    citySelect.disabled = !state;

    if (!state) {
        return;
    }

    stateCities[state].forEach((city) => {
        const option = document.createElement("option");
        option.value = city;
        option.textContent = city;
        citySelect.appendChild(option);
    });
}

fillStateOptions();

if (stateSelect && citySelect) {
    stateSelect.addEventListener("change", () => {
        fillCityOptions(stateSelect.value);
    });
}

if (phoneInput) {
    phoneInput.addEventListener("input", resetPhoneVerification);
}

if (verifyPhoneButton && phoneInput && phoneStatus) {
    verifyPhoneButton.addEventListener("click", () => {
        const phone = normalizePhone(phoneInput.value);

        if (!/^[6-9]\d{9}$/.test(phone)) {
            showPhoneStatus("Enter a valid 10 digit Indian mobile number.", "error");
            return;
        }

        isPhoneVerified = true;
        verifiedPhone = phone;
        showPhoneStatus("Phone number is available and verified.", "success");
    });
}

